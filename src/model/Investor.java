package model;

public class Investor {
	String name,contact_no,field_of_investment,website,emailid,max_investment;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getContact_no() {
		return contact_no;
	}

	public void setContact_no(String contact_no) {
		this.contact_no = contact_no;
	}

	public String getField_of_investment() {
		return field_of_investment;
	}

	public void setField_of_investment(String field_of_investment) {
		this.field_of_investment = field_of_investment;
	}

	public String getWebsite() {
		return website;
	}

	public void setWebsite(String website) {
		this.website = website;
	}

	public String getEmailid() {
		return emailid;
	}

	public void setEmailid(String emailid) {
		this.emailid = emailid;
	}

	public String getMax_investment() {
		return max_investment;
	}

	public void setMax_investment(String max_investment) {
		this.max_investment = max_investment;
	}
}
